export type FancyStyle = keyof typeof fancyAlphabets;

export interface FancyOptions {
  effect?: 'none' | 'underline' | 'strikethrough' | 'wavy' | 'slashThrough' | 'doubleUnderline' | 'crossAboveBelow';
  decoration?: 'none' | 'stars' | 'sparkles' | 'diamonds' | 'arrows' | 'flowers' | 'hearts' | 'music' | 'brackets';
}

export function generateFancyText(text: string, style: FancyStyle, options?: FancyOptions): string {
  let result = text;

  if (style === 'zalgo') {
    const zalgoUp = ['̍','̎','̄','̅','̿','̑','̆','̐','͒','͗','͑','̇','̈','̊','͂','̓','̈','͊','͋','͌','̃','̂','̌','͐','̀','́','̋','̏','̒','̓','̔','̽','̉','ͣ','ͤ','ͥ','ͦ','ͧ','ͨ','ͩ','ͪ','ͫ','ͬ','ͭ','ͮ','ͯ','̾','͛','͆','̚'];
    const zalgoDown = ['̖','̗','̘','̙','̜','̝','̞','̟','̠','̤','̥','̦','̩','̪','̫','̬','̭','̮','̯','̰','̱','̲','̳','̹','̺','̻','̼','ͅ','͇','͈','͉','͍','͎','͓','͔','͕','͖','͙','͚','̣'];
    const zalgoMid = ['̕','̛','̀','́','͘','̡','̢','̧','̨','̴','̵','̶','͜','͝','͞','͟','͠','͢','̸','̷','͡',' ҉'];
    
    result = text.split('').map(char => {
      if (char === ' ') return char;
      let newChar = char;
      for (let i = 0; i < 2; i++) newChar += zalgoUp[Math.floor(Math.random() * zalgoUp.length)];
      for (let i = 0; i < 1; i++) newChar += zalgoMid[Math.floor(Math.random() * zalgoMid.length)];
      for (let i = 0; i < 2; i++) newChar += zalgoDown[Math.floor(Math.random() * zalgoDown.length)];
      return newChar;
    }).join('');
  } else if (style !== 'normal') {
    const normal = fancyAlphabets.normal;
    const target = fancyAlphabets[style];
    
    if (style === 'block' || style === 'bracket' || style === 'emoji' || style === 'wavy' || style === 'strikethrough' || style === 'underline' || style === 'slashThrough' || style === 'doubleUnderline' || style === 'crossAboveBelow' || style === 'stars' || style === 'sparkles' || style === 'diamonds' || style === 'arrows') {
      result = text.split('').map(char => {
        const index = normal.indexOf(char);
        if (index !== -1) {
          if (style === 'block') {
             const blocks = ['░', '▒', '▓'];
             return blocks[index % 3] + char + blocks[(index + 1) % 3];
          } else if (style === 'bracket') return `《${char}》`;
          else if (style === 'emoji') return `♥${char}♥`;
          else if (style === 'wavy') return `${char}〰`;
          else if (style === 'strikethrough') return `${char}̶`;
          else if (style === 'underline') return `${char}̲`;
          else if (style === 'slashThrough') return `${char}̷`;
          else if (style === 'doubleUnderline') return `${char}̳`;
          else if (style === 'crossAboveBelow') return `${char}̽`;
          else if (style === 'stars') return `${char}★`;
          else if (style === 'sparkles') return `${char}✨`;
          else if (style === 'diamonds') return `${char}♦`;
          else if (style === 'arrows') return `${char}➳`;
        }
        return char;
      }).join('');
    } else {
      result = text.split('').map(char => {
        const index = normal.indexOf(char);
        if (index !== -1) {
          const targetArray = Array.from(target);
          return targetArray[index] || char;
        }
        return char;
      }).join('');
    }
  }

  // Apply effects
  if (options?.effect && options.effect !== 'none') {
    const effectMap: Record<string, string> = {
      underline: '̲',
      strikethrough: '̶',
      wavy: '〰',
      slashThrough: '̷',
      doubleUnderline: '̳',
      crossAboveBelow: '̽'
    };
    const effectChar = effectMap[options.effect];
    if (effectChar) {
      result = Array.from(result).map(char => char !== ' ' ? char + effectChar : char).join('');
    }
  }

  // Apply decorations
  if (options?.decoration && options.decoration !== 'none') {
    const decMap: Record<string, [string, string]> = {
      stars: ['★ ', ' ★'],
      sparkles: ['✨ ', ' ✨'],
      diamonds: ['♦ ', ' ♦'],
      arrows: ['➳ ', ' ➳'],
      flowers: ['✿ ', ' ✿'],
      hearts: ['♥ ', ' ♥'],
      music: ['♫ ', ' ♫'],
      brackets: ['【 ', ' 】']
    };
    const [prefix, suffix] = decMap[options.decoration] || ['', ''];
    result = `${prefix}${result}${suffix}`;
  }

  return result;
}
